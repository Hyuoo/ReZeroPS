def solution(m, n, startX, startY, balls):
    ans = []
    for x, y in balls:
        case = []
        
        if startY!=y or startX<x:
            case.append([-x,y])
        if startY!=y or startX>x:
            case.append([m+m-x,y])
        if startX!=x or startY<y:
            case.append([x,-y])
        if startX!=x or startY>y:
            case.append([x,n+n-y])
        
        dist = 999999
        for case_x, case_y in case:
            x_len = abs(case_x-startX)
            y_len = abs(case_y-startY)
            tmp = x_len**2+y_len**2
            dist = min(dist,tmp)
        ans.append(dist)
    
    return ans
